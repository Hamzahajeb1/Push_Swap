#include "../push_swap.h"

void	on_top_a(t_list **lst, int i , int j)
{
	if (j <= i / 2)
	{
		while ( 0 < j)
		{
			rotate_a(lst, 1);
			j--;
		}
	}
	if (j > i / 2)
	{
		j = i - j;
		while ( 0 < j)
		{
			rev_rotate_a(lst, 1);
			j--;
		}
	}
	return ;
}
