#include "../push_swap.h"

void	arr_sort(int **str, int len)
{
	int	i;
	int	t;
	int	j;

	i = 0;
	j = 1;
	while (i < len)
	{
		j = 1;
		while (j < len)
		{
			if ((*str)[j] < (*str)[j - 1])
			{
				t = (*str)[j - 1];
				(*str)[j - 1] = (*str)[j];
				(*str)[j] = t;
			}
			j++;
		}
		i++;
	}
}
